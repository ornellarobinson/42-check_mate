#include "check_mate.h"

int		check_pawn(char **tab, int i, int j)
{
	if (i > 0)
	{
		if (tab[i - 1][j - 1] == 'K' || tab[i - 1][j + 1] == 'K')
			return (1);
	}
	return (0);
}
